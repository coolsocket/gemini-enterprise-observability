const DICTS: Record<Locale, typeof ZH> = { zh: ZH, en: EN };

type Ctx = { locale: Locale; setLocale: (l: Locale) => void; t: (key: keyof typeof ZH) => string };
const I18nContext = createContext<Ctx>({ locale: "zh", setLocale: () => {}, t: (k) => String(k) });

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>(() => {
    try {
      const saved = localStorage.getItem("ge-locale");
      if (saved === "zh" || saved === "en") return saved;
    } catch {}
    return "zh";
  });
  useEffect(() => {
    try { localStorage.setItem("ge-locale", locale); } catch {}
  }, [locale]);
  const t = (key: keyof typeof ZH): string => DICTS[locale][key] ?? String(key);
  return <I18nContext.Provider value={{ locale, setLocale, t }}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
